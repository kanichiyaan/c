//Increment and Decrement Operator

#include<stdio.h>
int main()
{
    int a=5;
    printf("\nPre increment : %d",++a);
    printf("\nPost increment : %d",a++);
    printf("\nA : %d",a);
    printf("\nPost Decrement : %d",--a);
    printf("\nPost Decrement : %d",a--);
    printf("\nA : %d",a);
    return 0;
}